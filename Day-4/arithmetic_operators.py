#Operators

#Using Arithmetic Operators to Calculate the sum, difference, product, and quotient of a and b

a = 5
b = 10

def add():
    add = a + b
    return add

def sub():
    sub = a - b
    return sub

def mul():
    mul = a * b
    return mul

def quo():
    quo = a // b
    return quo

print("Add:", add())
print("Sub:", sub())
print("Mul:", mul())
print("Quotient:", quo())
