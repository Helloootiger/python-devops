#Operators

#Using Comparison Operators for the following comparison operators: <, >, <=, >=, ==, and !=

a = 5
b = 10

result1 = a < b
result2 = a > b
result3 = a <= b
result4 = a >= b
result5 = a == b
result6 = a != b

print("a < b:", result1)
print("a > b:", result2)
print("a <= b:", result3)
print("a >= b:", result4)
print("a == b:", result5)
print("a != b:", result6)
